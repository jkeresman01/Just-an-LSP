#pragma once

namespace justanlsp
{

//////////////////////////////////////////////////////////////
///
/// @enum CodeActionKind
///
/// @brief The kind of a code action.
///        The set of kinds is open and client needs to announce
///        the kinds it supports to the server during initialization.
///
//////////////////////////////////////////////////////////////
enum class CodeActionKind
{
    ///////////////////////////////////////////////////
    ///
    /// @brief No action will be provided
    ///
    //////////////////////////////////////////////////////////////
    EMPTY,

    ///////////////////////////////////////////////////
    ///
    /// @brief Provided code action is quick fix
    ///
    //////////////////////////////////////////////////////////////
    QUICK_FIX,

    ///////////////////////////////////////////////////
    ///
    /// @brief Provided code action is refactoring item
    ///
    //////////////////////////////////////////////////////////////
    REFACTOR,
};
} // namespace justanlsp
