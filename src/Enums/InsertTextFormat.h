#pragma once

namespace justanlsp
{

//////////////////////////////////////////////////////////////
///
/// @enum InsetTextFormat
///
/// @brief Defines whether the insert text in a completion
///        item should be interpreted as plain text or a snippet.
///
//////////////////////////////////////////////////////////////
enum class InsetTextFormat
{
    //////////////////////////////////////////////////////////////
    ///
    /// @brief The primary text to be inserted is treated as a plain string.
    ///
    //////////////////////////////////////////////////////////////
    PLAIN_TEXT = 1,

    //////////////////////////////////////////////////////////////
    ///
    /// @brief  The primary text to be inserted is treated as a snippet.
    ///
    //////////////////////////////////////////////////////////////
    SNIPPET = 1,
};
} // namespace justanlsp
