#pragma once

namespace justanlsp
{

//////////////////////////////////////////////////////////////
///
/// @enum RequestType
///
/// @brief Describes possible request types that client can make
///
///
//////////////////////////////////////////////////////////////
enum class RequestType
{
    //////////////////////////////////////////////////////////////
    ///
    /// @brief initialize request as defined by LSP protocol
    ///
    //////////////////////////////////////////////////////////////
    INITIALIZE,

    //////////////////////////////////////////////////////////////
    ///
    /// @brief shutdown request as defined by LSP protocol
    ///
    //////////////////////////////////////////////////////////////
    SHUTDOWN,

    //////////////////////////////////////////////////////////////
    ///
    /// @brief initialized request as defined by LSP protocol
    ///
    //////////////////////////////////////////////////////////////
    INITIALIZED,

    //////////////////////////////////////////////////////////////
    ///
    /// @brief textDocument/didOpen request as defined by LSP protocol
    ///
    //////////////////////////////////////////////////////////////
    TEXT_DOCUMENT_DID_OPEN,

    //////////////////////////////////////////////////////////////
    ///
    /// @brief textDocument/didChange request as defined by LSP protocol
    ///
    //////////////////////////////////////////////////////////////
    TEXT_DOCUMENT_DID_CHANGE,

    //////////////////////////////////////////////////////////////
    ///
    /// @brief textDocument/hover request as defined by LSP protocol
    ///
    //////////////////////////////////////////////////////////////
    TEXT_DOCUMENT_HOVER,

    //////////////////////////////////////////////////////////////
    ///
    /// @brief textDocument/completion request as defined by LSP protocol
    ///
    //////////////////////////////////////////////////////////////
    TEXT_DOCUMENT_COMPLETION,

    //////////////////////////////////////////////////////////////
    ///
    /// @brief Unkown request type
    ///
    //////////////////////////////////////////////////////////////
    UNKNOWN,

    //////////////////////////////////////////////////////////////
    ///
    /// @brief Max number of request types
    ///
    //////////////////////////////////////////////////////////////
    EXIT,

    //////////////////////////////////////////////////////////////
    ///
    /// @brief Max number of request types
    ///
    //////////////////////////////////////////////////////////////
    COUNT,
};

} // namespace justanlsp
