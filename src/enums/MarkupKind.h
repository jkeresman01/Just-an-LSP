#pragma once

namespace justanlsp
{

//////////////////////////////////////////////////////////////
///
/// @enum MarkupKind
///
/// @brief Describes the content type that an LSP client supports
///
//////////////////////////////////////////////////////////////
enum class MarkupKind
{
    //////////////////////////////////////////////////////////////
    ///
    /// @brief Plain text is supported as a content format
    ///
    //////////////////////////////////////////////////////////////
    PLAIN_TEXT,

    //////////////////////////////////////////////////////////////
    ///
    /// @brief Markdown is supported as a content format
    ///
    //////////////////////////////////////////////////////////////
    MARKDOWN,

    //////////////////////////////////////////////////////////////
    ///
    /// @brief Used for getting max number of possible markup kind
    ///        values
    ///
    //////////////////////////////////////////////////////////////
    OPTIONS_NO,
};

} // namespace justanlsp
