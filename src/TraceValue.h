#pragma once

namespace justanlsp
{
//////////////////////////////////////////////////////////////
///
/// @enum TraceValue
///
/// @brief A TraceValue represents the level of verbosity with
///        which the server systematically reports its execution
///        trace using $/logTrace notifications.
///        The initial trace value is set by the client at
///        initialization and can be modified later using
///        the $/setTrace notification.
///
//////////////////////////////////////////////////////////////
enum class TraceValue
{

    //////////////////////////////////////////////////////////////
    ///
    /// @brief Trace Value 'off'
    ///
    //////////////////////////////////////////////////////////////
    OFF,

    //////////////////////////////////////////////////////////////
    ///
    /// @brief Trace Value 'messages'
    ///
    //////////////////////////////////////////////////////////////
    MESSAGES,

    //////////////////////////////////////////////////////////////
    ///
    /// @brief Trace Value 'verbose'
    ///
    //////////////////////////////////////////////////////////////
    VERBOSE,
};
} // namespace justanlsp
